    private void btnGuiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGuiActionPerformed
          tentk = txtTenTaiKhoan.getText();  // Lấy tên tài khoản từ trường nhập liệu
        email = txtEmail.getText();        // Lấy email từ trường nhập liệu

    // Kiểm tra tên tài khoản và email
            DangNhapController controller = new DangNhapController();
        if (controller.isTenTaiKhoanAndEmailValid(tentk, email)) {
            FormLayLaiMatKhau form=new FormLayLaiMatKhau();
            form.show();
            this.dispose();
        } else {
            // Nếu không hợp lệ, thông báo lỗi
            JOptionPane.showMessageDialog(this, "Tên tài khoản hoặc email không chính xác!", "Lỗi", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGuiActionPerformed

